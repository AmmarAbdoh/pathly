/**
 * Get templates by category
 */
export const getTemplatesByCategory = (category: string): GoalTemplate[] => {
  return GOAL_TEMPLATES.filter(template => template.category === category);
};

/**
 * Get translated goal templates based on language
 */
export const getGoalTemplates = (language: Language = 'en'): GoalTemplate[] => {
  return GOAL_TEMPLATES_BASE.map(template => {
    const translation = getGoalTemplateTranslation(template.id, language);
    return {
      ...template,
      title: translation.title || template.title,
      description: translation.description || template.description,
    };
  });
};

// Export default English templates
export const GOAL_TEMPLATES = GOAL_TEMPLATES_BASE;

/**
 * Get a template by ID
 */
export const getTemplateById = (id: string, language: Language = 'en'): GoalTemplate | undefined => {
  const templates = getGoalTemplates(language);
  return templates.find(template => template.id === id);
};
